def _ask(prompt, valid=None, default=None):
    hint = f" [{'/'.join(valid)}]" if valid else ""
    if default:
        hint += f"  (default: {default})"
    while True:
        raw = input(f"\n  {YELLOW}>{RESET} {prompt}{hint}: ").strip()
        if not raw and default is not None:
            return default
        if not raw:
            continue
        if valid is None:
            return raw
        up = raw.upper()
        if up in [v.upper() for v in valid]:
            return up
        print(f"  {RED}  Choose from: {', '.join(valid)}{RESET}")


def _pick(items, prompt, label_fn=None, allow_none=False):
    if not items:
        print(f"  {RED}  No files found.{RESET}")
        return None
    for i, item in enumerate(items, 1):
        label = label_fn(item) if label_fn else str(item)
        star = f"  {CYAN}*{RESET}" if i == 1 else "   "
        print(f"  {DIM}{i:2d}.{RESET}{star} {label}")
    if allow_none:
        print(f"   {DIM}0.   [none / skip]{RESET}")
    while True:
        lo, hi = (0 if allow_none else 1), len(items)
        raw = input(f"\n  {YELLOW}>{RESET} {prompt} [{lo}-{hi}]: ").strip()
        try:
            idx = int(raw)
            if allow_none and idx == 0:
                return None
            if 1 <= idx <= len(items):
                return items[idx - 1]
        except ValueError:
            pass
        print(f"  {RED}  Enter a number.{RESET}")


