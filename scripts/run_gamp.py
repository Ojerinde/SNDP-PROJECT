def _label(path: Path, role: str = None, week: int = None,
           dow: int = None, year: int = None, doy: int = None) -> str:
    ac = path.name[:3].lower()
    gname = ""
    note = ""
    if role and year and doy and week is not None:
        gname_val = gamp_name_for(role, path, year, doy, week, dow)
        if gname_val != path.name:
            gname = f"  {DIM}-> {gname_val}{RESET}"
    if role == "dcb" and path.suffix.lower() == ".bia":
        note = f"  {RED}[BIA/OSB format — NOT compatible with GAMP, use BSX instead]{RESET}"
    elif role == "atx" and path.name.lower() != "igs14.atx":
        note = f"  {YELLOW}[may fail in old GAMP parser; prefer igs14.atx]{RESET}"
    elif role in ("sp3", "clk", "erp") and ac not in GAMP_AC_LIST:
        note = f"  {YELLOW}[AC '{ac}' not in GAMP list -> renamed to 'cod']{RESET}"
    return f"{path.name}  {DIM}[{ac}]{RESET}{gname}{note}"


