def copy_file_as(src: Path, run_dir: Path, gamp_name: str, label: str) -> bool:
    if not src.exists():
        print(f"  {RED}  x {label}: not found ({src}){RESET}")
        return False
    dst = run_dir / gamp_name
    shutil.copy2(src, dst)
    size_mb = src.stat().st_size / 1_048_576
    if src.name != gamp_name:
        print(f"  {GREEN}  OK {label}{RESET}  "
              f"{DIM}{src.name} -> {gamp_name}  ({size_mb:.1f} MB){RESET}")
    else:
        print(f"  {GREEN}  OK {label}{RESET}  {DIM}({size_mb:.1f} MB){RESET}")
    return True


def copy_and_strip_obs(src: Path, run_dir: Path) -> str:
    """Copy obs file to run_dir.  RINEX 2 is copied directly (no trailing records
    possible).  RINEX 3 is stripped of any trailing special event records to
    prevent GAMP's norm(cte)<=1.0 error.  Returns destination filename."""
    dst_name = src.name
    rinex_ver = _rinex_version(src)
    is_rinex2 = (rinex_ver is not None and rinex_ver <
                 3.0) or _is_rinex2_obs(src.name)
    if is_rinex2:
        shutil.copy2(src, run_dir / dst_name)
        size_mb = src.stat().st_size / 1_048_576
        print(f"  {GREEN}  OK obs{RESET}  {DIM}(RINEX 2, {size_mb:.1f} MB){RESET}")
    else:
        n = strip_trailing_records(src, run_dir / dst_name)
        size_mb = (run_dir / dst_name).stat().st_size / 1_048_576
        if n > 0:
            print(f"  {GREEN}  OK obs{RESET}  "
                  f"{DIM}(RINEX 3, {size_mb:.1f} MB, stripped {n} trailing records){RESET}")
        else:
            print(
                f"  {GREEN}  OK obs{RESET}  {DIM}(RINEX 3, {size_mb:.1f} MB){RESET}")
    return dst_name


