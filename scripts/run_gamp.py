# RINEX 2: {site4}{doy}{session}.{yy}o  —  years 80-29 → 1980-2029
# RINEX 3: .rnx  (site9_R_{year}{doy}... naming handled by regex fallback below)
OBS_EXTS = {".rnx", ".obs"} | {f".{yy:02d}o" for yy in range(0, 30)}


def find_obs_files():
    if not DATA_DIR.exists():
        return []
    return sorted(
        [f for f in DATA_DIR.iterdir()
         if f.suffix.lower() in OBS_EXTS
         or re.match(r".*\.\d{2}[onqg]$", f.name, re.I)],
        key=lambda f: f.name,
    )


def extract_date(filename: str):
    """Return (year_int, doy_int) from filename, or (None, None)."""
    m = re.search(r"_(\d{4})(\d{3})0000_", filename)
    if m:
        return int(m.group(1)), int(m.group(2))
    m = re.match(r"[a-z0-9]{4}(\d{3})\d+\.\d{2}[onqg]", filename, re.I)
    if m:
        yr2 = int(re.search(r"\.(\d{2})[onqg]", filename, re.I).group(1))
        year = 2000 + yr2 if yr2 < 80 else 1900 + yr2
        return year, int(m.group(1))
    return None, None


def station_from_obs(filename: str) -> str:
    return filename[:4].upper()


def _rinex_version(path: Path):
    """Best-effort RINEX version read from first line; returns float or None."""
    try:
        with open(path, "r", encoding="ascii", errors="replace") as fh:
            line = fh.readline()
        return float(line[:9].strip())
    except Exception:
        return None


def _obs_date_from_header(path: Path):
    """Best-effort (year, doy) from TIME OF FIRST OBS in header.

    Works for both RINEX 2 and RINEX 3 when filename does not carry date.
    Returns (None, None) on failure.
    """
    try:
        with open(path, "r", encoding="ascii", errors="replace") as fh:
            for _ in range(300):
                line = fh.readline()
                if not line:
                    break
                if "TIME OF FIRST OBS" in line:
                    nums = re.findall(r"[-+]?\d+(?:\.\d+)?", line[:43])
                    if len(nums) >= 3:
                        y = int(float(nums[0]))
                        m = int(float(nums[1]))
                        d = int(float(nums[2]))
                        if y < 100:
                            y = 2000 + y if y < 80 else 1900 + y
                        doy = date(y, m, d).timetuple().tm_yday
                        return y, doy
                if "END OF HEADER" in line:
                    break
    except Exception:
        pass
    return None, None


def find_products_for_date(year: int, doy: int) -> dict:
    """Return dict of lists for each product type, filtered by date.

    Supports both:
      RINEX 3 long-name: COD0MGXFIN_{year}{doy}0000_...  (new, post-2019)
      RINEX 2 short-name: cod{week}{dow}.sp3              (old, GAMP-native)
    RINEX 4 nav files (BRD4*) are excluded -- GAMP cannot parse them.
    BSX DCB files are returned regardless of date (2017 BSX works for 2026).
    """
    r3_pattern = f"{year}{doy:03d}"
    gps_week, _dow = gps_week_dow(year, doy)
    r2_prefix = rf"[a-zA-Z0-9]{{3}}{gps_week:04d}"

    def _search(subdir, exts):
        d = PROD_DIR / subdir
        if not d.exists():
            return []

        def _match(f):
            if f.suffix.lower() not in exts:
                return False
            n = f.name
            if r3_pattern in n:
                return True
            if re.match(r2_prefix, n):
                return True
            return False

        return sorted([f for f in d.iterdir() if _match(f)], key=lambda f: f.name)

    def _search_nav(exts):
        """Like _search('nav') but excludes RINEX 4 files (BRD4* prefix),
        and explicitly includes classic RINEX-2 brdm{doy}0.{yy}p naming."""
        all_nav = _search("nav", exts)

        # Ensure classic RINEX-2 nav files are discoverable even if they don't
        # carry week/date tokens in long-name form.
        nav_dir = PROD_DIR / "nav"
        yy = year % 100
        brdm_pat = re.compile(rf"brdm{doy:03d}0\.{yy:02d}p$", re.I)
        if nav_dir.exists():
            for f in nav_dir.iterdir():
                if f.suffix.lower() in exts and brdm_pat.search(f.name):
                    if f not in all_nav:
                        all_nav.append(f)
            all_nav = sorted(all_nav, key=lambda p: p.name)

        rnx3 = [f for f in all_nav if not f.name.upper().startswith("BRD4")]
        rnx4 = [f for f in all_nav if f.name.upper().startswith("BRD4")]
        if rnx4:
            print(f"  {YELLOW}  Note: {len(rnx4)} RINEX-4 nav file(s) excluded "
                  f"(BRD4* -- not supported by GAMP):{RESET}")
            for f in rnx4:
                print(f"    {DIM}  - {f.name}{RESET}")
        return rnx3

    def _search_dcb():
        """BSX files (any date) first, then date-matched BIA/DCB files.
        Using a 2017 BSX for 2026 is intentional -- satellite DCBs are stable."""
        d = PROD_DIR / "dcb"
        if not d.exists():
            return []
        bsx = sorted([f for f in d.iterdir()
                      if f.suffix.lower() == ".bsx"], key=lambda f: f.name)
        other = sorted([f for f in d.iterdir()
                        if f.suffix.lower() in {".bia", ".dcb"}
                        and (r3_pattern in f.name
                             or re.match(r2_prefix, f.name))],
                       key=lambda f: f.name)
        return bsx + other

    atx_dir = PROD_DIR / "atx"
    atx_files = sorted(atx_dir.glob("*.atx"), key=lambda p: (p.name.lower()
                       != "igs14.atx", p.name.lower())) if atx_dir.exists() else []
    if not any(p.name.lower() == "igs14.atx" for p in atx_files) and GAMP_IGS14_FALLBACK.exists():
        # Expose known-good igs14.atx even if user has only igs20/igs20_* in products.
        atx_files = [GAMP_IGS14_FALLBACK] + atx_files

    return {
        "sp3": _search("sp3",   {".sp3"}),
        "clk": _search("clk",   {".clk"}),
        "erp": _search("erp",   {".erp"}),
        "gim": _search("ionex", {".inx", ".ionex",
                                 ".17i", ".18i", ".19i", ".20i", ".21i",
                                 ".22i", ".23i", ".24i", ".25i", ".26i"}),
        "nav": _search_nav({".rnx", ".p",
                            ".17p", ".18p", ".19p", ".20p", ".21p",
                            ".22p", ".23p", ".24p", ".25p", ".26p",
                            ".n", ".g", ".l"}),
        "atx": atx_files,
        "dcb": _search_dcb(),
        "snx": _search("snx",   {".snx"}),
    }


