    with open(run_dir / "site.crd", "w", newline="\n") as fh:
        fh.write("# Reference coordinates for accuracy evaluation\n")
        fh.write("# Source: ITRF2020\n")
        fh.write(f"{su:<4s}  {x:15.4f}  {y:15.4f}  {z:15.4f}\n")
    print(f"  {GREEN}  OK site.crd{RESET}  ({su} ITRF2020)")


