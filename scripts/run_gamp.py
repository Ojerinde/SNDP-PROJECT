def main():
    _enable_ansi()
    banner()

    if not GAMP_EXE.exists():
        print(f"{RED}ERROR: GAMP not found:\n  {GAMP_EXE}{RESET}")
        sys.exit(1)

    # ------------------------------------------------------------------
    # Step 1: Observation file
    # ------------------------------------------------------------------
    section("Select Observation File", step=1)
    obs_files = find_obs_files()
    if not obs_files:
        print(f"  {RED}No observation files found in {DATA_DIR}{RESET}")
        sys.exit(1)

    print(f"  Files in  {DIM}{DATA_DIR}{RESET}:\n")
    obs = _pick(obs_files, "Select obs file", label_fn=lambda f: f.name)
    if obs is None:
        sys.exit(0)

    station = station_from_obs(obs.name)
    year, doy = extract_date(obs.name)
    if year is None or doy is None:
        y_hdr, d_hdr = _obs_date_from_header(obs)
        if y_hdr is not None and d_hdr is not None:
            year, doy = y_hdr, d_hdr

    rinex_ver = _rinex_version(obs)
    is_rinex2 = (rinex_ver is not None and rinex_ver <
                 3.0) or _is_rinex2_obs(obs.name)
    if year is not None and doy is not None:
        date_info = f"{year}/DOY {doy:03d}"
    else:
        date_info = "date=unknown"
    print(f"\n  {GREEN}  Selected:{RESET}  {obs.name}  "
          f"{DIM}(station={station}, {date_info}){RESET}")
    if year is None:
        print(f"  {YELLOW}  Warning: could not extract date from filename{RESET}")

    if year and doy:
        gps_week, gps_dow = gps_week_dow(year, doy)
        dow_names = ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"]
        print(
            f"  {DIM}  GPS week {gps_week}, DOW {gps_dow} ({dow_names[gps_dow]}){RESET}")
    else:
        gps_week = gps_dow = None

    # ------------------------------------------------------------------
    # Step 2: Scenario
    # ------------------------------------------------------------------
    section("Select Processing Scenario", step=2)
    if is_rinex2:
        print(f"  {YELLOW}RINEX-2 observation detected.{RESET}")
        print(f"  {YELLOW}  Note: GAMP often rejects all epochs with UC multi-GNSS "
              f"(C/D) on mixed RINEX-2 files.{RESET}")
        print(f"  {YELLOW}  Recommended for RINEX-2: Scenario B (GPS IF).{RESET}\n")

    recommended_key = "B" if is_rinex2 else "C"
    print()
    for key, sc in SCENARIOS.items():
        star = f"  {CYAN}*{RESET} recommended" if key == recommended_key else "  "
        print(f"  {BOLD}{key}{RESET}  {sc['name']}{star}")
        print(f"      {DIM}{sc['desc']}{RESET}\n")

    scenario_key = _ask("Enter scenario", valid=list(
        SCENARIOS.keys()), default=recommended_key)

    if is_rinex2 and scenario_key in ("C", "D"):
        print(f"\n  {YELLOW}Warning:{RESET} scenario {scenario_key} with RINEX-2 can produce "
              f"0 valid epochs (continuous GF cycle-slip rejection).")
        keep = _ask("Keep scenario anyway", valid=["Y", "N"], default="N")
        if keep == "N":
            scenario_key = "B"
            print(
                f"  {GREEN}  Switched to:{RESET}  B  -  {SCENARIOS['B']['name']}")

    sc = SCENARIOS[scenario_key]
    print(f"\n  {GREEN}  Selected:{RESET}  {scenario_key}  -  {sc['name']}")

    # ------------------------------------------------------------------
    # Step 3: Products
    # ------------------------------------------------------------------
    section("Select Precise Products", step=3)
    products = {}   # role -> Path

    if year:
        print(f"  Searching products/ for date {year}/{doy:03d} ...\n")
        avail = find_products_for_date(year, doy)
    else:
        avail = {k: []
                 for k in ["sp3", "clk", "erp", "gim", "nav", "atx", "dcb", "snx"]}

    def lbl(p, role):
        return _label(p, role=role, week=gps_week, dow=gps_dow,
                      year=year, doy=doy)

    def _prompt(role, display_label, required):
        """Show file list and return selected Path (or None if skipped)."""
        files = avail.get(role, [])

        if not files:
            if not required:
                print(f"  {DIM}  {display_label}: none found — skipping{RESET}")
                return None
            print(
                f"  {YELLOW}  {display_label}: no matching file in products/{role}/{RESET}")
            raw = input(
                f"  {YELLOW}>{RESET} Full path (or blank to skip): ").strip()
            return Path(raw) if raw else None

        print(f"\n  {display_label}:  ({len(files)} found)")
        if len(files) == 1:
            # Auto-select the only option, but show it clearly
            print(f"   {DIM} 1.   {lbl(files[0], role)}{RESET}")
            return files[0]
        # Multiple options — let user choose
        return _pick(files, f"Select {display_label}",
                     label_fn=lambda p: lbl(p, role),
                     allow_none=not required)

    if not any(sc["needs"].values()):
        print(f"  Scenario A uses broadcast nav only — no precise products needed.")
    else:
        if sc["needs"]["sp3"]:
            products["sp3"] = _prompt("sp3", "SP3 orbit", required=True)
        if sc["needs"]["clk"]:
            products["clk"] = _prompt("clk", "CLK clock", required=True)
        if sc["needs"]["erp"]:
            products["erp"] = _prompt(
                "erp", "ERP earth-rotation", required=True)
        if sc["needs"]["dcb"]:
            products["dcb"] = _prompt(
                "dcb",
                "DCB signal bias  (optional — GAMP expects CAS0MGXRAP_...BSX format)",
                required=False)
        if sc["needs"]["gim"]:
            products["gim"] = _prompt(
                "gim", "GIM ionosphere (IONEX)", required=True)

    # Nav always needed
    products["nav"] = _prompt("nav", "Broadcast navigation", required=True)

    # ATX — always selectable (user should choose)
    if not any(p.name.lower() == "igs14.atx" for p in avail.get("atx", [])):
        print(f"\n  {YELLOW}Warning:{RESET} no igs14.atx found in products/atx.")
        print(
            f"  {YELLOW}  Old GAMP builds often fail on igs20.atx (error in reading atx).{RESET}")

    products["atx"] = _prompt(
        "atx",
        "ATX antenna corrections  (will be saved as igs14.atx for GAMP)",
        required=(scenario_key != "A"))

    # SINEX — optional, auto-selected if available
    snx_files = avail.get("snx", [])
    if snx_files and gps_week:
        print(f"\n  {DIM}SINEX (reference coords for error column):{RESET}")
        print(
            f"   {DIM} 1.   {snx_files[0].name} -> {gamp_snx_name(gps_week)}{RESET}")
