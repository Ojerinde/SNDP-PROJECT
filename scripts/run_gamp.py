def write_cfg(run_dir: Path, obs_name: str, scenario_key: str,
              file_map: dict) -> Path:
    sc = SCENARIOS[scenario_key]
    obs_path = str(run_dir / obs_name).replace("/", "\\")
    ionconstraint = 1 if scenario_key == "D" else 0

    map_lines = [f"#   {orig}  ->  {gname}"
                 for orig, gname in file_map.items()] or ["#   (no renaming needed)"]

    content = CFG_TEMPLATE.format(
        timestamp=datetime.now().isoformat(sep=" ", timespec="seconds"),
        station=station_from_obs(obs_name),
        scenario_key=scenario_key,
        scenario_name=sc["name"],
        run_dir=str(run_dir).replace("/", "\\"),
        obs_path=obs_path,
        posmode=sc["posmode"],
        navsys=sc["navsys"],
        ionoopt=sc["ionoopt"],
        inpfrq=sc["inpfrq"],
        ionconstraint=ionconstraint,
        file_map_comment="\n".join(map_lines),
    )
    cfg = run_dir / "run.cfg"
    cfg.write_text(content, encoding="utf-8")
    return cfg


