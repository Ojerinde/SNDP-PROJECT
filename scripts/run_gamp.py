def _ac_from_r3(filename: str) -> str:
    """Extract lowercase 3-letter AC from RINEX-3 filename prefix."""
    return filename[:3].lower()


def _ac_gamp(filename: str) -> str:
    """Return AC if GAMP-compatible, else 'cod' as fallback."""
    ac = _ac_from_r3(filename)
    return ac if ac in GAMP_AC_LIST else "cod"


def gamp_sp3_name(filename: str, week: int, dow: int) -> str:
    return f"{_ac_gamp(filename)}{week:04d}{dow}.sp3"


def gamp_clk_name(filename: str, week: int, dow: int) -> str:
    return f"{_ac_gamp(filename)}{week:04d}{dow}.clk"


def gamp_erp_name(filename: str, week: int) -> str:
    return f"{_ac_gamp(filename)}{week:04d}7.erp"


def gamp_nav_name(year: int, doy: int) -> str:
    return f"brdm{doy:03d}0.{year % 100:02d}p"


def gamp_ionex_name(year: int, doy: int) -> str:
    return f"CODG{doy:03d}0.{year % 100:02d}I"


def gamp_atx_name() -> str:
    return "igs14.atx"     # GAMP only knows up to igs14 era


def gamp_snx_name(week: int) -> str:
    return f"igs{week:04d}.snx"


def gamp_dcb_name(year: int, doy: int) -> str:
    return f"CAS0MGXRAP_{year:04d}{doy:03d}0000_01D_01D_DCB.BSX"


def _is_rinex2_obs(name: str) -> bool:
    """True for RINEX 2 obs: {site4}{doy}{session}.{yy}o  e.g. cut02440.17o"""
    return bool(re.match(r"[a-z0-9]{4}\d{3}[0-9a-z]\.\d{2}[odg]$", name, re.I))


def gamp_name_for(role: str, path: Path, year: int, doy: int,
                  week: int, dow: int) -> str:
    """Return the GAMP-expected filename for a product."""
    if role == "sp3":
        return gamp_sp3_name(path.name, week, dow)
    elif role == "clk":
        return gamp_clk_name(path.name, week, dow)
    elif role == "erp":
        return gamp_erp_name(path.name, week)
    elif role == "nav":
        return gamp_nav_name(year, doy)
    elif role == "atx":
        return gamp_atx_name()
