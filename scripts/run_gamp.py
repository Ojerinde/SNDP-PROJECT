def strip_trailing_records(src: Path, dst: Path) -> int:
    """Copy RINEX obs file, stripping trailing special records (epoch flag 4+).

    GAMP's Windows binary (32-bit) fails to parse getLastTime() when a
    RINEX file ends with special event records (e.g. GLONASS frequency slot
    tables appended by Spider software).  Stripping them fixes the error:
      *** ERROR: norm(cts,6)<=1.0||norm(cte,6)<=1.0

    Returns the number of lines stripped.
    """
    with open(src, "r", encoding="ascii", errors="replace") as fh:
        lines = fh.readlines()

    # Find last real data epoch: line starts with '>' and has a 4-digit year
    last_epoch_line = 0
    for i, line in enumerate(lines):
        if line.startswith(">") and len(line) > 10:
            try:
                if float(line[2:6]) > 2000:
                    last_epoch_line = i
            except ValueError:
                pass

    # Extend to include all satellite obs lines after that epoch
    cutoff = last_epoch_line
    for i in range(last_epoch_line + 1, min(last_epoch_line + 200, len(lines))):
        if lines[i].startswith(">"):
            break
        cutoff = i

    n_stripped = len(lines) - cutoff - 1
    with open(dst, "w", encoding="ascii", newline="\n") as fh:
        fh.writelines(lines[: cutoff + 1])
    return n_stripped


